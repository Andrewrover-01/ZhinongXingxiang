# 主题列表：(主题名, [(类型标签, 模板元组列表), ...])
TOPIC_GROUPS = [
    ("病害", [(d[0], "disease", d[1], d[2]) for d in DISEASES]),
    ("虫害", [(p[0], "pest",    p[1], p[2]) for p in PESTS]),
    ("技术", [(t[0], "technique", t[1], []) for t in TECHNIQUES]),
    ("政策", [(po[0], "policy",   po[1], []) for po in POLICIES]),
    ("气象", [(w[0], "weather",  w[1], []) for w in WEATHER]),
]


def _build_disease_content(crop: str, name: str, symptom: str, measures: list[str]) -> str:
    steps = "；".join(f"({i+1}){m}" for i, m in enumerate(measures))
    return (
        f"{crop}{name}是{crop}生产中的重要病害之一。"
        f"主要症状：{symptom}。"
        f"发病条件：高湿、高温、通风不良时易流行。"
        f"防治措施：{steps}。"
        f"建议在发病初期及时用药，连续防治2~3次，每次间隔7天。"
    )


def _build_pest_content(crop: str, name: str, harm: str, measures: list[str]) -> str:
    steps = "；".join(f"({i+1}){m}" for i, m in enumerate(measures))
    return (
        f"{name}是危害{crop}的常见害虫。"
        f"危害特点：{harm}。"
        f"防治策略：{steps}。"
        f"注意轮换使用不同作用机理的药剂，延缓抗药性产生；避免在天敌活跃期施药。"
    )


def _build_technique_content(crop: str, tech: str, desc: str) -> str:
    return (
        f"在{crop}生产中推行{tech}技术，具体做法：{desc}。"
        f"该技术适用于{crop}全生育期管理，可有效提升产量和品质，降低生产成本。"
        f"建议结合当地气候和土壤条件因地制宜地实施。"
    )


def _build_policy_content(crop: str, policy: str, desc: str) -> str:
    return (
        f"涉及{crop}生产的政策——{policy}：{desc}。"
        f"种植{crop}的农户可依据相关规定申请享受该政策支持，具体申请流程请咨询当地农业农村局。"
    )


def _build_weather_content(crop: str, event: str, desc: str) -> str:
    return (
        f"极端天气对{crop}的影响及应对——{event}：{desc}。"
        f"农户应密切关注气象预警，提前做好防灾减灾准备，减少{crop}因气象灾害造成的损失。"
    )


def get_seeds(min_count: int = 500) -> List[dict]:
    """程序化生成农业知识种子数据列表。

    每条记录包含：
        id       -- 自增整数
        title    -- 标题字符串
        content  -- 知识正文
        tags     -- 标签列表（作物 + 类目关键词）

    Args:
        min_count: 最少生成条数，默认 500。

    Returns:
        list[dict]，长度 >= min_count。
    """
    seeds: list[dict] = []
    # 使用 itertools.cycle 循环遍历作物和主题，直到达到最小数量
    crop_cycle = itertools.cycle(CROPS)

    # 展开所有主题条目：(group_name, item_name, category_tag, content_builder_args)
    topic_pool: list[tuple] = []
    for _group_name, items in TOPIC_GROUPS:
        for item in items:
            topic_pool.append(item)

    topic_cycle = itertools.cycle(topic_pool)

    idx = 1
    while len(seeds) < min_count:
        crop = next(crop_cycle)
        item = next(topic_cycle)
        item_name, category_tag, *rest = item

        if category_tag == "disease":
            symptom, measures = rest[0], rest[1]
            title = f"{crop}{item_name}防治技术"
            content = _build_disease_content(crop, item_name, symptom, measures)
            tags = [crop, "病害", item_name, "防治"]
        elif category_tag == "pest":
            harm, measures = rest[0], rest[1]
            title = f"{crop}{item_name}防治指南"
            content = _build_pest_content(crop, item_name, harm, measures)
            tags = [crop, "虫害", item_name, "防治"]
        elif category_tag == "technique":
            desc = rest[0]
            title = f"{crop}{item_name}技术要点"
            content = _build_technique_content(crop, item_name, desc)
            tags = [crop, "栽培技术", item_name]
        elif category_tag == "policy":
            desc = rest[0]
            title = f"{crop}相关政策：{item_name}"
            content = _build_policy_content(crop, item_name, desc)
            tags = [crop, "农业政策", item_name]
        elif category_tag == "weather":
            desc = rest[0]
            title = f"{crop}气象灾害应对：{item_name}"
            content = _build_weather_content(crop, item_name, desc)
            tags = [crop, "气象灾害", item_name]
        else:
            continue

        seeds.append({"id": idx, "title": title, "content": content, "tags": tags})
        idx += 1

    return seeds


